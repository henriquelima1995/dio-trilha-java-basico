package edu.henrique.tiposvariaveis;
public class Inteiro {
   public static void main(String[] args) {
    // Tipos Primitivos
    //============ Tipos Inteiros ============

    // Números inteiros pequenos até 127
    byte b =100; // 8 bits

    // Números inteiros médios até 32.767 
    short s = 10000; // 16 bits

    //Números inteiros padrão até 2.147.483.647
    int i = 100000; // 32 bits

    // Números inteiros grandes até 9.223.372.036.854.775.807
    long l = 100000L; // 64 bits
   }
}
