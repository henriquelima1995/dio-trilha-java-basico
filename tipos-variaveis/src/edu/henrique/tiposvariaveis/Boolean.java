package edu.henrique.tiposvariaveis;

public class Boolean {
    public static void main(String[] args) {
        // Tipos Primitivos
        //============ Tipos Boolean ============

        // Valores lógicos, verdadeiro ou falso
        boolean isTrue = true;  // 1 bit, representa verdadeiro
        boolean isFalse = false; // 1 bit, representa falso

        // Exemplos de uso
        if (isTrue) {
            System.out.println("É verdadeiro!");
        } else {
            System.out.println("É falso!");
        }
        System.out.println(isFalse);
    }
}
